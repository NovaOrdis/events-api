package io.novaordis.events.api.metric.os;

import io.novaordis.events.api.event.Property;
import io.novaordis.events.api.metric.MetricDefinition;
import io.novaordis.utilities.os.OSType;

/**
 * Represents the definition of a metric whose values can be obtained either by reading and parsing a file, or by
 * executing an external O/S command on the OSSourceBase metric source associated with this definition. Once the
 * file content is read, or the command is executed and the stdout is collected, the content such produced must be
 * passed to the appropriate parseFileContent()/parseCommandOutput() method, which possesses the knowledge to extract
 * the reading (property instance) from the content.
 *
 * The metric definition does not read and parse the file internally, but delegate this task to the calling layer
 * to allow for the file to be read once, and the content to be shared among multiple metric definitions, instead of
 * relying on each individual metric definition to do the I/O operations. This becomes important when the file resides
 * on a remote metric source, such as a RemoteOS. It is also important to read the file once, if multiple metrics
 * extract their readings from it, to insure the fact that the values are associated with just one timestamp, and not
 * read during successive operations.
 *
 * OSMetricDefinitions work for metrics that can be collected from LocalOS and RemoteOS metric sources.
 *
 * @see OSSourceBase
 * @see LocalOS
 * @see RemoteOS
 *
 * @author Ovidiu Feodorov <ovidiu@novaordis.com>
 * @since 6/5/17
 */
public interface OSMetricDefinition extends MetricDefinition {

    // Constants -------------------------------------------------------------------------------------------------------

    // Static ----------------------------------------------------------------------------------------------------------

    // Public ----------------------------------------------------------------------------------------------------------

    /**
     * @param osType a valid OSType.
     *
     * @return the command to be executed to obtain the metric value, on the OS corresponding to the given osType.
     * Usually the command's stdout has to be parsed to extract the individual metric value. The metric definition
     * implementation contains the parsing logic.
     *
     * If the metric is not available on the specified OS, the method will return null.
     *
     * @see OSMetricDefinition#parseCommandOutput(String)
     */
    String getCommand(OSType osType);

    /**
     * @param commandExecutionStdout the stdout generated by the execution of the command indicated by getCommand().
     *                               It may be null in case the metric is not available on the target system, and the
     *                               method must be prepared to handle that, by manufacturing a null-value property.
     *
     * @return the metric value extracted from the command stdout, parsed according to the output expected by the
     * command provided by getCommand(). If a value cannot be successfully extracted because of invalid command output,
     * or even if the command output is null, the method must return a valid Property instance containing a null value.
     * The method must not throw an exception in this case. The implementation should also log as WARN more details on
     * why collection failed, in case of a non-null argument.
     */
    Property parseCommandOutput(String commandExecutionStdout);

}
